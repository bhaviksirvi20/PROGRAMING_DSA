// logical oparator 
#include <iostream>

using namespace std;
int main()
{
    int a = 10 , b = 20;

    cout << " 1 : 10 + 20 = ";
    cout << a + b << endl;
    
    cout << " 2 : 10 * 20 = ";
    cout << a * b << endl;

    cout << " 3 : 10 / 20 = ";
    cout << a / b << endl;

    cout << " 4 : 10 - 20 = ";
    cout << a - b << endl;

    cout << " 5 : 10 % 20 = ";
    cout << a % b << endl;

    cout << "----------------------" << endl;
    return 0;
}
